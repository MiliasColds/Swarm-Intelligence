
/*
 * Generic Point class, contains an two-dimensional coordinate point. I figured we could throw in some
 * generic distance/etc. things in here if we needed to.
 */
public class Point {
	public int xPos;
	public int yPos;
	
	public Point(int xPos, int yPos)
	{
		this.xPos = xPos;
		this.yPos = yPos;
	}
}
